public class Pilipenko_2_1 {
    public static void main(String[] args) {
        // (комментарий) объявление и инициализация переменных
        byte a = 127;
        short b = 17;
        int c = 100;
        long d = 567004L;
        float f = 1.345f; // f - обязательно
        double e = 5.98765;
        char ch = 'G'; // одинарные кавычки
        boolean Sandra = true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(f);
        System.out.println(e);
        System.out.println(ch);
        System.out.println(Sandra);


    }

}
